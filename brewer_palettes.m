function pal = brewer_palettes(name)
% BREWER_PALETTES - return color brewer color palettes
% USAGE
%     pal = brewer_palettes(name)
%
% INPUTS:
%     names: name of palette. Current options: Dark2
%
% OUTPUTS:
%     pal: Nx3 array of matlab color values
%
% (c) Timothy W. Hilton, UNM, Mar 2012

if strcmp( name, 'Dark2' )
    pal = [ 27, 158, 119; ...
            217, 95, 2; ...
            117, 112, 179; ...
            231, 41, 138; ...
            102, 166, 30 ];
    pal = pal / 255.0;

elseif strcmp( name, 'Paired' )
    pal = [ 166, 206, 227; ...
            31, 120, 180; ...
            178, 223, 138; ...
            51, 160, 44; ...
            251, 154, 153; ...
            227, 26, 28; ...
            253, 191, 111; ...
            255, 127, 0 ];
    pal = pal / 255.0;
end
